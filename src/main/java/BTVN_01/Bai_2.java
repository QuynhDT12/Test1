package BTVN_01;

public class Bai_2 {
    public static void main(String[] args) {
        int a = 3, b = 2;

        // Print the result of adding two integers
        System.out.printf("Tổng 2 số nguyên là: %d%n" , a+b);
        // Print the result of subtracting two integers
        System.out.printf("Hiệu 2 số nguyên là: %d%n", a-b);
        // Print the result of multiplying two integers
        System.out.printf("Tích 2 số nguyên là: %d%n", a*b);
        // Print the result of dividing two integers and getting the remainder
        System.out.printf("Chia 2 số nguyên lấy phần dư là: : %d", a%b);
    }
}
